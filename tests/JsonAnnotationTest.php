            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->dtoGen->generate($queries[0], false, $this->extGen);

        // The generated City.php code should have the extension trait injected
        $this->assertArrayHasKey('City', $r['jsonDtos']);
        $cityCode = $r['jsonDtos']['City']['code'];
        $this->assertStringContainsString('use CityExtension;', $cityCode);
    }

    public function test_main_dto_extension_includes_json_column_as_array_property(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
            FROM countries
            INNER JOIN cities ON cities.country_id = countries.id
            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->dtoGen->generate($queries[0], false, $this->extGen);

        // The parent DTO extension should list `cities` as @property array
        $parentExt = null;
        foreach ($r['extensions'] as $ext) {
            if ($ext->traitName === 'GetCountryWithCitiesRowExtension') {
                $parentExt = $ext;
                break;
            }
        }

        $this->assertNotNull($parentExt, 'Parent DTO extension not found');
        // Extension scaffold should list `cities` as @property array
        $this->assertMatchesRegularExpression('/@property array\s+\$cities/', $parentExt->scaffoldCode);
    }

    public function test_json_dto_extension_scoped_when_scoped_dtos_enabled(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
            FROM countries
            INNER JOIN cities ON cities.country_id = countries.id
            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->scopedDtoGen->generate($queries[0], true, $this->extGen);

        $cityExt = null;
        foreach ($r['extensions'] as $key => $ext) {
            if ($ext->traitName === 'CityExtension') { $cityExt = $ext; break; }
        }

        $this->assertNotNull($cityExt, 'CityExtension not found in scoped mode');
        // Scoped extension must include the subdirectory in its relPath
        $this->assertStringContainsString('Country', $cityExt->relPath);
        $this->assertStringContainsString('GetCountryWithCities', $cityExt->relPath);
    }
}
