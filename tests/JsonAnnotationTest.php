    // =========================================================================
    // Extensions — @json DTO gets its own extension trait
    // =========================================================================

    public function test_json_dto_extension_is_generated_when_extgen_provided(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
            FROM countries
            INNER JOIN cities ON cities.country_id = countries.id
            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->dtoGen->generate($queries[0], false, $this->extGen);

        // Extension for City JSON DTO must exist
        $this->assertNotEmpty($r['extensions']);
        $extKeys = array_keys($r['extensions']);
        $cityExtKey = array_filter($extKeys, fn($k) => str_contains($k, 'City'));
        $this->assertNotEmpty($cityExtKey, 'CityExtension not found in extensions');
    }

    public function test_json_dto_extension_has_correct_trait_name(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
            FROM countries
            INNER JOIN cities ON cities.country_id = countries.id
            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->dtoGen->generate($queries[0], false, $this->extGen);

        $cityExt = null;
        foreach ($r['extensions'] as $ext) {
            if ($ext->traitName === 'CityExtension') {
                $cityExt = $ext;
                break;
            }
        }

        $this->assertNotNull($cityExt, 'CityExtension not found');
        $this->assertStringContainsString('trait CityExtension', $cityExt->scaffoldCode);
    }

    public function test_json_dto_extension_has_property_tags_from_schema(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
            FROM countries
            INNER JOIN cities ON cities.country_id = countries.id
            WHERE countries.id = :id
            GROUP BY countries.id;
        SQL);

        $r = $this->dtoGen->generate($queries[0], false, $this->extGen);

        $cityExt = null;
        foreach ($r['extensions'] as $ext) {
            if ($ext->traitName === 'CityExtension') { $cityExt = $ext; break; }
        }

        $this->assertNotNull($cityExt);
        // Extension scaffold should list City's schema columns as @property
        $this->assertStringContainsString('@property int    $id', $cityExt->scaffoldCode);
        $this->assertStringContainsString('@property string $name', $cityExt->scaffoldCode);
        $this->assertStringContainsString('@property string $slug', $cityExt->scaffoldCode);
    }

    public function test_json_dto_code_has_use_extension_injected(): void
    {
        $queries = $this->analyze(<<<SQL
            -- @name GetCountryWithCities
            -- @class Country
            -- @returns :one
            -- @json cities City
            SELECT
                countries.id,
                countries.name,
                JSON_ARRAYAGG(JSON_OBJECT('id', cities.id)) AS cities
